    private boolean isValidInput ()
    {
        try{
            Integer.parseInt(txtValue.getText());
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Nhap Value la so nguyen");
            return false;
        }
        
        try{
            Integer.parseInt(txtMin.getText());
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Nhap Min la so nguyen");
            return false;
        }
        
        try{
            Integer.parseInt(txtMax.getText());
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Nhap Max la so nguyen");
            return false;
        }
        return true;
    }
    
