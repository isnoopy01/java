    private void btnKhoiTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKhoiTaoActionPerformed
        // TODO add your handling code here:
        if (isValidInput())
        {
            int value = Integer.parseInt(txtValue.getText());
            int min = Integer.parseInt(txtMin.getText());
            int max = Integer.parseInt(txtMax.getText());
            if (value < min || value > max)
            {
                JOptionPane.showMessageDialog(null, "Value phai nam giua min va max");
            }
            else
            {
                Number n = new Number (value,min,max);
                JOptionPane.showMessageDialog(null, "Khoi tao number thanh cong");
                System.out.println(n.toString());
                clearFields();
            }
        }
    }//GEN-LAST:event_btnKhoiTaoActionPerformed

    private void btnLuuTruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuTruActionPerformed
        // TODO add your handling code here:
        if (isValidInput())
        {
            int value = Integer.parseInt(txtValue.getText());
            int min = Integer.parseInt(txtMin.getText());
            int max = Integer.parseInt(txtMax.getText());
            if (value < min || value > max)
            {
                JOptionPane.showMessageDialog(null, "Value phai nam giua min va max");
            }
            else
            {
                Number n = new Number (value,min,max);
                if (saveNumberToFile(n))
                {
                    JOptionPane.showMessageDialog(null, "Luu tru number thanh cong");
                    clearFields();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Luu tru khong thanh cong");
                }
                
                
                
            }
        }
        
    }//GEN-LAST:event_btnLuuTruActionPerformed

    private boolean saveNumberToFile(Number n)
    {
        if (!n.isPrime())
        {
            JOptionPane.showMessageDialog(null, n.getValue()+" khong phai so nguyen to");
            return false;
        }
        try{
            File f = new File("number.txt");
            FileWriter fw = new FileWriter(f);
            BufferedWriter bw = new BufferedWriter(fw);
            bw.write(n.toString());
            bw.close();
            return true;
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, "Khong mo duoc file");
            return false;
        }
    }
    
